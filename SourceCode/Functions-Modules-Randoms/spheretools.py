import math


#A function named getvolume.
#This function will return the volume of a sphere.
#The function accepts one parameter: r
def getvolume(r) :
  return 4 * math.pi * r ** 2

#A function named getsurfacearea.
#This function will return the surface area of a sphere.
#The function accepts one parameter: r
def getsurfacearea(r) :
  return 4 / 3 * math.pi * r ** 3




